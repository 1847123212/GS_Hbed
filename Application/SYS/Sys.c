#include "Sys.h"
/***************************************************
*								us延时子函数
*						 n=1时，约为11us
****************************************************/
void delay_11us(uint16 n)
{
	while (n--);
}
/***************************************************
*								ms延时子函数
*						 n=1时，约为1ms
****************************************************/
void delay_ms(uint16 n)
{
	uint8 m=120;
	while (n--)
		while (m--);
}